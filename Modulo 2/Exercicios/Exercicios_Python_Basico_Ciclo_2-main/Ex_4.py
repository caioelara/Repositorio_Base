# Calcule a média das notas utilizando um loop while e também um loop for


# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

notas = ['9.5', '10', '6.75', '5.5']

# LOOP WHILE
soma=0
contator=0
while contator<len(notas):
    nota=float(nota[contator])
    soma=soma+nota
    contator=contator+1
print(soma/len(notas))




